// Slice 140 — OpenAPI 3.1 spec generator.
//
// Emits the canonical `docs/openapi.yaml` from the in-tree RouteSpecs
// slice. Pure-Go, no external deps beyond the standard library; output
// is YAML written line-by-line in a stable, sorted order so two
// back-to-back runs produce byte-identical output (load-bearing for the
// BLOCKING `openapi-drift-check` CI guard — slice 140 D3).
//
// Design constraints honored:
//
//   - **AC-2 deterministic.** No map iteration; routes are sorted by
//     (Path, Method) before emission. Strings are quoted with `%q`-free
//     YAML-safe quoting where ambiguity is possible.
//
//   - **P0-A1 every operation carries security.** The Tier field maps
//     to one of three buckets: `none` (no `security` block — only the
//     genuinely-public set), `bearer`, or `adminBearer`. Drift-detect
//     verifies the chi handler's OPA gate maps to the documented tier.
//
//   - **P0-A3 internal endpoints carry `x-internal: true`.** Set from
//     the RouteSpec.Internal field; Redoc UI filters these from public
//     render.
//
//   - **P0-A5 spec ≤ 500 KB.** Tested in generator_test.go. The v1
//     spec is roughly 30-40 KB at 176 routes — well under budget.
//
//   - **P0-A7 NO gRPC endpoints.** The `.proto` files in `proto/` are
//     the gRPC source of truth. The RouteSpecs slice contains only
//     REST routes.
//
//   - **P0-A8 NO `.gen.yaml` sibling.** Single output file at
//     `docs/openapi.yaml`. The drift-detect script asserts the
//     committed file is in lockstep with `RouteSpecs`.

package openapi

import (
	"fmt"
	"io"
	"regexp"
	"sort"
	"strings"
)

// Generate writes the OpenAPI 3.1 YAML representation of the given
// routes to w. Sort order is stable: paths grouped by alphabetical
// path, methods grouped by HTTP-method alphabetical order within
// each path.
//
// Returns an error only if the underlying writer fails.
func Generate(w io.Writer, routes []RouteSpec) error {
	// Defensive copy: never mutate caller's slice via sort.
	sorted := make([]RouteSpec, len(routes))
	copy(sorted, routes)
	sort.SliceStable(sorted, func(i, j int) bool {
		if sorted[i].Path != sorted[j].Path {
			return sorted[i].Path < sorted[j].Path
		}
		return sorted[i].Method < sorted[j].Method
	})

	// Header — info + servers + tags + security schemes + paths.
	if _, err := io.WriteString(w, header()); err != nil {
		return err
	}

	// Tags — derived from the unique Tag field. Sorted alphabetically.
	tags := uniqueTags(sorted)
	if _, err := io.WriteString(w, "tags:\n"); err != nil {
		return err
	}
	for _, tag := range tags {
		if _, err := fmt.Fprintf(w, "  - name: %s\n", tag); err != nil {
			return err
		}
	}
	if _, err := io.WriteString(w, "\n"); err != nil {
		return err
	}

	// Paths section. Grouped by path; multiple methods under each path.
	if _, err := io.WriteString(w, "paths:\n"); err != nil {
		return err
	}
	pathGroups := groupByPath(sorted)
	for _, path := range pathGroups.paths {
		if _, err := fmt.Fprintf(w, "  %s:\n", path); err != nil {
			return err
		}
		for _, r := range pathGroups.byPath[path] {
			if err := writeOperation(w, r); err != nil {
				return err
			}
		}
	}

	// Components — security schemes.
	if _, err := io.WriteString(w, componentsSection()); err != nil {
		return err
	}
	return nil
}

