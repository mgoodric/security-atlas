// componentsSection returns the components block declaring the security
// schemes the operations reference. Three tiers:
//
//   - bearer: any authenticated credential (Bearer <token>).
//   - adminBearer: admin-only credential. Same wire shape as bearer;
//     distinguished by the credstore IsAdmin flag + the slice-035 OPA
//     middleware role check.
//   - (no scheme): public endpoints — health probes, version,
//     install-state, calendar.ics (URL-token authenticated inline),
//     and the /auth/* sign-in routes (no bearer yet at sign-in time).
func componentsSection() string {
	return `
components:
  securitySchemes:
    bearer:
      type: http
      scheme: bearer
      bearerFormat: opaque
      description: |
        Any authenticated bearer credential issued by
        ` + "`POST /v1/admin/credentials`" + ` (slice 034) or established via the
        ` + "`/auth/*`" + ` OIDC / local sign-in flow. Token is opaque; the
        platform validates against the credstore or DB-backed apikeystore.
    adminBearer:
      type: http
      scheme: bearer
      bearerFormat: opaque
      description: |
        Admin-only bearer credential. Same wire shape as ` + "`bearer`" + `, but
        the platform rejects requests whose credential is not marked
        ` + "`is_admin=true`" + ` via the slice-035 OPA middleware AND the
        per-handler defense-in-depth role probe.
  schemas:
    Error:
      type: object
      required: [error]
      properties:
        error:
          type: string
          description: Human-readable error message.
      description: |
        Generic error envelope. Specific operations may return richer
        error shapes (validation details, field-level messages); this
        is the lowest-common-denominator contract.
    Ack:
      type: object
      description: |
        Empty acknowledgment envelope. Used by mutation endpoints that
        do not return a representation of the mutated resource (e.g.
        ` + "`POST /v1/policies/{id}/publish`" + `).
`
}

// uniqueTags returns the sorted unique Tag values from the routes.
func uniqueTags(routes []RouteSpec) []string {
	seen := map[string]struct{}{}
	for _, r := range routes {
		seen[r.Tag] = struct{}{}
	}
	out := make([]string, 0, len(seen))
	for tag := range seen {
		out = append(out, tag)
	}
	sort.Strings(out)
	return out
}

// pathGroupings holds the routes grouped by path with the path list
// pre-sorted for stable emission.
type pathGroupings struct {
	paths  []string
	byPath map[string][]RouteSpec
}

func groupByPath(routes []RouteSpec) pathGroupings {
	out := pathGroupings{byPath: map[string][]RouteSpec{}}
	seenPaths := map[string]struct{}{}
	for _, r := range routes {
		if _, ok := seenPaths[r.Path]; !ok {
			out.paths = append(out.paths, r.Path)
			seenPaths[r.Path] = struct{}{}
		}
		out.byPath[r.Path] = append(out.byPath[r.Path], r)
	}
	// Paths are already in sorted order because the caller sorted by
	// (Path, Method) before calling. Methods within each path are
	// likewise sorted. Explicit sort here is defensive.
	sort.Strings(out.paths)
	for path := range out.byPath {
		methods := out.byPath[path]
		sort.SliceStable(methods, func(i, j int) bool { return methods[i].Method < methods[j].Method })
		out.byPath[path] = methods
	}
	return out
}

// writeOperation emits one OpenAPI operation entry. The format is:
//
//	    get:
//	      summary: <Summary>
//	      tags: [<Tag>]
//	      x-internal: true          # only when Internal is true
//	      security:                 # omitted when Tier is "none"
//	        - bearer: []
//	      parameters:               # only when path contains {placeholders}
//	        - name: id
//	          in: path
//	          required: true
//	          schema: { type: string }
//	      responses:
//	        default:
//	          description: ...
//	          content:
//	            application/json:
//	              schema:
//	                $ref: '#/components/schemas/Ack'
//
// The indentation is two-space, matching the rest of the file. The
// emission is line-by-line, never map-marshalled — keeps the output
// strictly deterministic regardless of Go map iteration order.
func writeOperation(w io.Writer, r RouteSpec) error {
	method := strings.ToLower(r.Method)

	if _, err := fmt.Fprintf(w, "    %s:\n", method); err != nil {
		return err
	}
	if _, err := fmt.Fprintf(w, "      summary: %s\n", yamlSafe(r.Summary)); err != nil {
		return err
	}
	if _, err := fmt.Fprintf(w, "      tags: [%s]\n", r.Tag); err != nil {
		return err
	}
	// operationId is stable: lowered METHOD-PATH-slug (slashes / braces
	// stripped). Useful for codegen.
	if _, err := fmt.Fprintf(w, "      operationId: %s\n", operationID(r)); err != nil {
		return err
	}
	if r.Internal {
		if _, err := io.WriteString(w, "      x-internal: true\n"); err != nil {
			return err
		}
	}
	if r.Tier != "none" {
		if _, err := io.WriteString(w, "      security:\n"); err != nil {
			return err
		}
		if _, err := fmt.Fprintf(w, "        - %s: []\n", r.Tier); err != nil {
			return err
		}
	}
	// Parameters: extract {placeholders} from the path.
	params := pathParameters(r.Path)
	if len(params) > 0 {
		if _, err := io.WriteString(w, "      parameters:\n"); err != nil {
			return err
		}
		for _, p := range params {
			if _, err := fmt.Fprintf(w, "        - name: %s\n", p); err != nil {
				return err
			}
			if _, err := io.WriteString(w, "          in: path\n"); err != nil {
				return err
			}
			if _, err := io.WriteString(w, "          required: true\n"); err != nil {
				return err
			}
			if _, err := io.WriteString(w, "          schema:\n            type: string\n"); err != nil {
				return err
			}
		}
	}
	// Responses — single `default` ref to the Ack envelope. v1 spec
	// does not commit to per-operation response schemas; follow-on
	// slice will refine. Operations that genuinely return bodies (most
	// GETs) currently document with the Ack placeholder — the
	// integration is path/method-stable, body shape is documented in
	// the slice docs until the spec catches up.
	if _, err := io.WriteString(w, "      responses:\n"); err != nil {
		return err
	}
	if _, err := io.WriteString(w, "        default:\n"); err != nil {
		return err
	}
	if _, err := io.WriteString(w, "          description: Default response envelope.\n"); err != nil {
		return err
	}
	if _, err := io.WriteString(w, "          content:\n"); err != nil {
		return err
	}
	if _, err := io.WriteString(w, "            application/json:\n"); err != nil {
		return err
	}
	if _, err := io.WriteString(w, "              schema:\n"); err != nil {
		return err
	}
	if _, err := io.WriteString(w, "                $ref: '#/components/schemas/Ack'\n"); err != nil {
		return err
	}
	return nil
}

// pathParamRE matches OpenAPI / chi path placeholders.
var pathParamRE = regexp.MustCompile(`\{([A-Za-z0-9_]+)\}`)

// pathParameters returns the {placeholder} names from a chi path in
// declaration order.
func pathParameters(path string) []string {
	matches := pathParamRE.FindAllStringSubmatch(path, -1)
	out := make([]string, 0, len(matches))
	for _, m := range matches {
		out = append(out, m[1])
	}
	return out
}

// operationID builds a stable operation id from (Method, Path). Used
// by downstream codegen tools that key per-operation client methods
// off operationId. The transform is deterministic and avoids the
// reserved-character set: slashes → hyphens, braces dropped, colons
// → "_" (for the `:push` / `:upload-bundle` / `:finalize` actions).
func operationID(r RouteSpec) string {
	out := strings.ToLower(r.Method) + r.Path
	// Replace slashes with hyphens; drop braces; replace colons.
	out = strings.ReplaceAll(out, "/", "-")
	out = strings.ReplaceAll(out, "{", "")
	out = strings.ReplaceAll(out, "}", "")
	out = strings.ReplaceAll(out, ":", "_")
	out = strings.ReplaceAll(out, ".", "_")
	out = strings.Trim(out, "-")
	return out
}

// yamlSafe returns a YAML-safe rendering of s. The current generator
// only needs to handle the Summary field (one-line ASCII strings); we
// quote with double-quote-and-escape when the content contains a YAML
// special character (colon followed by space, leading/trailing
// whitespace, etc.). For the generator's current input set, none of
// the summaries triggers quoting, so the format stays plain.
func yamlSafe(s string) string {
	// Quote if the string starts with a YAML-special character or
	// contains `: ` (the YAML key/value separator), which would
	// otherwise be misparsed as nested keys.
	if needsYAMLQuoting(s) {
		// Double-quote + escape backslashes + double quotes.
		escaped := strings.ReplaceAll(s, `\`, `\\`)
		escaped = strings.ReplaceAll(escaped, `"`, `\"`)
		return `"` + escaped + `"`
	}
	return s
}

// needsYAMLQuoting returns true when the string would be ambiguous
// without quoting. Conservative: prefers to over-quote rather than
// risk silently misparsing.
func needsYAMLQuoting(s string) bool {
	if s == "" {
		return true
	}
	if strings.HasPrefix(s, " ") || strings.HasSuffix(s, " ") {
		return true
	}
	// YAML special starts: list/map/anchor/tag/quote/etc.
	switch s[0] {
	case '-', '?', ':', ',', '[', ']', '{', '}', '#', '&', '*', '!', '|', '>', '\'', '"', '%', '@', '`':
		return true
	}
	if strings.Contains(s, ": ") {
		return true
	}
	return false
}
